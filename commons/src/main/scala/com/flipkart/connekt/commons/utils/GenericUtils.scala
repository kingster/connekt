import scala.reflect.ClassTag
import scala.reflect.runtime.universe._

object GenericUtils {

  def typeToClassTag[T: TypeTag]: ClassTag[T] = {
    ClassTag[T]( typeTag[T].mirror.runtimeClass( typeTag[T].tpe ) )
  }



}
